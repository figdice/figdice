    /**
     * The problem comes from two causes:
     * 1/ When authoring a FigDice template file, mostly relevant for
     * HTML output, one does not want to declare two hundred XML entities
     * in a Doctype. Yet, one wants the legacy HTML entities to be accepted
     * by the FigDice parser.
     * 2/ The HHVM implementation of the php libxml does not accept recursive
     * entities in XML doctype. So that it is not possible, anyway, to declare
     * unmodified entities.
     * 
     * @param string $string
     * @return string
     */
